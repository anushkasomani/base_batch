export default function HomepagePreview() {
  const [nfts, setNfts] = useState([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchNFTs = async () => {
      try {
        setLoading(true);
        const provider = new BrowserProvider(
          window.ethereum || "https://sepolia.base.org"
        );
        const contract = new Contract(NFT_CONTRACT_ADDRESS, NFT_ABI, provider);

        const totalSupply = await contract.totalSupply();
        const nftList = [];

        for (let i = 0; i < Number(totalSupply); i++) {
          const tokenId = await contract.tokenByIndex(i);
          const owner = await contract.ownerOf(tokenId);
          const tokenURI = await contract.tokenURI(tokenId);

          // If IPFS, convert to gateway URL
          let metadataUrl = tokenURI;
          if (tokenURI.startsWith("ipfs://")) {
            metadataUrl = tokenURI.replace("ipfs://", "https://ipfs.io/ipfs/");
          }

          // Fetch metadata
          let image = "";
          try {
            const res = await fetch(metadataUrl);
            const metadata = await res.json();
            image = metadata.image || metadata.image_url || "";
            if (image.startsWith("ipfs://")) {
              image = image.replace("ipfs://", "https://ipfs.io/ipfs/");
            }
          } catch {
            image = "";
          }

          nftList.push({
            tokenId: tokenId.toString(),
            owner,
            image,
            metadataUrl,
          });
        }
        setNfts(nftList);
      } catch (err) {
        console.error("Error fetching NFTs:", err);
      }
      setLoading(false);
    };

    fetchNFTs();
  }, []);

  return (
    <div className="relative min-h-screen w-full">
      <div
        className="fixed inset-0 w-full h-full bg-cover bg-center bg-no-repeat"
        style={{ backgroundImage: "url(/igb.png)" }}
      />
      <div className="relative min-h-screen w-full flex items-center justify-center p-4">
        <div className="w-4/5 min-h-screen overflow-y-auto bg-white bg-opacity-90 rounded-lg shadow-lg p-4 flex items-center justify-center flex-col space-y-1">
          <div className="mb-6">
            <h2 className="text-lg font-semibold text-gray-700 mb-3">
              Minted NFTs
            </h2>
            {loading ? (
              <div className="text-gray-500">Loading...</div>
            ) : (
              <div className="grid grid-cols-1 sm:grid-cols-3 gap-4">
                {nfts.map((nft) => (
                  <PetCard
                    key={nft.tokenId}
                    petId={nft.tokenId}
                    imageSrc={nft.image}
                    owner={nft.owner}
                    metadataUrl={nft.metadataUrl}
                  />
                ))}
              </div>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}
